/**
 * CSS Custom Format with Typography Documentation
 * Generates CSS custom properties with helpful typography scale documentation
 */
export const cssCustomFormat = {
  name: "css/variables-with-typography-doc",
  format: function({ dictionary }) {
    const header = `
/* ================================================================================================== */
/*  Don't edit directly • Generated on ${new Date().toUTCString()} • muibook.com                                        */
/* ================================================================================================== */

/* ================================================================================================== */
/*  Typography on the Web • Orchestrating Vertical Rhythm                                             */
/* ================================================================================================== */
/*  An introduction to baseline grids and vertical rhythm in web typography.                          */
/*  The video below explores the history of type through letterpress and how these                    */
/*  traditional principles can guide modern decisions.                                                */
/* ================================================================================================== */
/*  Watch: https://youtu.be/FFGSy0EfA9Y by Michael Trilford                                           */
/* ================================================================================================== */

/* ================================================================================================== */
/*                                         Type Scale                                                 */
/* -------------------------------------------------------------------------------------------------- */
/*   6px |  7px |  8px |  9px | 10px |  11px |  12px |  14px |  16px |  18px |  21px |  24px |  36px  */
/*  48px | 60px | 72px | 84px | 96px | 108px | 120px | 132px | 144px | 156px | 168px | 180px | 192px  */
/* ================================================================================================== */

/* ================================================================================================= */
/*                                           Leading Scale                                           */
/* ------------------------------------------------------------------------------------------------- */
/* ======================================= 18 / 11  =  1.636 ======================================= */
/* ======================================= 18 / 12  =  1.500 ======================================= */
/* ======================================= 21 / 14  =  1.500 ======================================= */
/* ======================================= 24 / 16  =  1.500 ======================================= */
/* ======================================= 24 / 18  =  1.333 ======================================= */
/* ======================================= 24 / 21  =  1.143 ======================================= */
/* ======================================= 36 / 24  =  1.500 ======================================= */
/* ======================================= 48 / 36  =  1.333 ======================================= */
/* ======================================= 60 / 48  =  1.250 ======================================= */
/* ======================================= 72 / 60  =  1.200 ======================================= */
/* ======================================= 84 / 72  =  1.167 ======================================= */
/* ======================================= 96 / 84  =  1.143 ======================================= */
/* ====================================== 108 / 96  =  1.125 ======================================= */
/* ================================================================================================= */

`;

    // Generate CSS variables from tokens
    const variables = dictionary.allTokens
      .map((token) => {
        const varName = token.path.join("-");
        return `  --${varName}: ${token.value};`;
      })
      .join("\n");

    return `${header}:where(html) {\n${variables}\n}\n`;
  },
};
